//Arithmetic Operators......
#include <iostream>
using namespace std;
int main() {
  int x = 10;
  int y = 3;

  cout << (x + y) << "\n"; // 13
  cout << (x - y) << "\n"; // 7
  cout << (x * y) << "\n"; // 30
  cout << (x / y) << "\n"; // 3  (integer division!)
  cout << (x % y) << "\n"; // 1

  int z = 5;
  ++z;
  cout << z << "\n"; // 6
  --z;
  cout << z << "\n"; // 5

  return 0;
}
