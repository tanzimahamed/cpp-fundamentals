// Calculate the Area of a Rectangle
#include <iostream>
using namespace std;

int main() {
  // Create integer variables
  int length = 4;
  int width = 6;

  // Calculate the area of a rectangle
  int area = length * width;

  // Print the variables
  cout << "Length is: " << length << "\n";
  cout << "Width is: " << width << "\n";
  cout << "Area of the rectangle is: " << area << "\n";

  return 0;
}